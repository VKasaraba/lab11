import re

pattern = re.compile(
    '(.+\[22/Mar/2009:((19:((2[5-9])|([3-5][0-9])):((2[1-9])|[3-5][0-9]))|2[0-3]:[0-5][0-9]:[0-5][0-9]) \+\d{4}\] ".+" 4[0-9][0-9])|'
    '(.+\[2[3-8]/Mar/2009:(([0-1][0-9])|(2[0-3])):[0-5][0-9]:[0-5][0-9] \+\d{4}\] ".+" 4[0-9][0-9])|'
    '(.+\[29/Mar/2009:((((0[0-9])|(1[0-1])):[0-5][0-9]:[0-5][0-9])|(12:00:00)) \+\d{4}\] ".+" 4[0-9][0-9])')

for i, line in enumerate(open('logs.txt')):
    for match in re.finditer(pattern, line):
        print(match.group())
